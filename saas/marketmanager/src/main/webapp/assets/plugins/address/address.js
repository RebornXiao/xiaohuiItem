XAddress.prototype.find = function (items, id) {
    if (typeof(items[id]) == "undefined")
        return false;
    return items[id];
}

XAddress.prototype.setData = function (ui, json, selected_id) {
    $.each(json, function (k, v) {
        if (k == selected_id) {
            var option = '<option value="' + k + '" selected="selected">' + v + '</option>';
            ui.append(option);
        } else {
            var option = '<option value="' + k + '" >' + v + '</option>';
            ui.append(option);
        }
    });
}

XAddress.prototype.now_district_id = 0;
XAddress.prototype.post_func = null;

XAddress.prototype.fillOption = function (ui, items, targetid, selected_id) {
    var json = this.find(items, targetid);
    if (json) {
        this.setData(ui, json, selected_id);
    }
    ui.select2("val", "");
}

function showLocation(province_id, city_id, district_id, street_id, street_list, post_func) {

    var _loc_province = $('#loc_province');
    var _loc_city = $('#loc_city');
    var _loc_district = $('#loc_district');
    var _loc_street = $('#loc_street');

    var loc = new XAddress();
    loc.post_func = post_func;

    var title = ['省份', '地级市', '区、县、镇', '街道'];
    $.each(title, function (k, v) {
        title[k] = '<option value=\"k\">' + v + '</option>';
    });

    if (street_list != null && street_list != "" && district_id != 0) {
        loc.streets[district_id] = street_list;
    }

    _loc_province.append(title[0]);
    _loc_city.append(title[1]);
    _loc_district.append(title[2]);

    _loc_province.select2();
    _loc_city.select2();
    _loc_district.select2();

    if (_loc_street) {
        _loc_street.append(title[3]);
        _loc_street.select2();
    }

    _loc_province.change(function () {
        _loc_city.empty();
        _loc_city.append(title[1]);
        loc.fillOption(_loc_city, loc.citys, _loc_province.val());
        _loc_city.change();
    });

    _loc_city.change(function () {
        _loc_district.empty();
        _loc_district.append(title[2]);
        loc.fillOption(_loc_district, loc.districts, _loc_city.val());
        _loc_district.change();
    });

    _loc_district.change(function () {

        if (_loc_street) {
            _loc_street.empty();
            _loc_street.append(title[3]);

            if (!$.isNumeric(_loc_district.val())) {
                return;
            }
            loc.now_district_id = _loc_district.val();

            var streets = loc.streets[loc.now_district_id];

            //如果有数据，则执行数据，否则，POST拿数据
            if (typeof(streets) == "undefined") {
                if (loc.post_func != null) {
                    loc.post_func(loc.now_district_id, function (post_id, json) {
                        if (json.code == 0) {
                            var streets_data = json.datas;
                            loc.streets[post_id] = streets_data;
                            if (post_id == loc.now_district_id) {
                                //刷新
                                loc.fillOption(_loc_street, loc.streets, post_id);
                                _loc_street.change();
                            }
                        } else {
                            alert("获取失败，" + json.msg);
                        }
                    });
                }
            } else {
                loc.fillOption(_loc_street, loc.streets, loc.now_district_id);
                _loc_street.change();
            }
        } else {
            if (!$.isNumeric(_loc_district.val())) {
                return;
            }
            if (loc.post_func != null) {
                loc.post_func(_loc_district.val());
            }
        }
    });

    //if (_loc_street) {
        //_loc_street.change(function () {
        //    $('input[name=location_id]').val($(this).val());
        //});
    //}

    if (province_id && province_id != 0) {
        loc.setData(_loc_province, loc.provinces, province_id);

        if (city_id && city_id != 0) {
            loc.fillOption(_loc_city, loc.citys, province_id, city_id);

            if (district_id && district_id != 0) {
                loc.fillOption(_loc_district, loc.districts, city_id, district_id);

                if (street_id && street_id != 0) {
                    loc.fillOption(_loc_street, loc.streets, district_id, street_id);
                } else {
                    _loc_district.change();
                }
            } else {
                _loc_city.change();
            }
        } else {
            _loc_province.change();
        }
    } else {
        loc.setData(_loc_province, loc.provinces, -1);
    }

}

